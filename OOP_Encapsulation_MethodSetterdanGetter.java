package PraktikumOOP;

public class OOP_Encapsulation_MethodSetterdanGetter {

    String nama;
    int angka;

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public int getAngka() {
        return angka;
    }

    public void setAngka(int angka) {
        this.angka = angka;
    }


}
